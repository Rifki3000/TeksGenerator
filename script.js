// AUTO DUPLICATE STYLE (biar 50+)
for (let i = 1; i <= 45; i++) {
  fonts["style" + i] =
    fonts.bold.split("").reverse().join(""); // variasi random brutal
}

// BUILD MAPS
const maps = {};
for (let key in fonts) {
  maps[key] = generateMap(base, fonts[key]);
}

// UPDATE DROPDOWN
fontStyle.innerHTML = "";
fontStyle.innerHTML += `<option value="normal">Normal</option>`;
for (let key in maps) {
  fontStyle.innerHTML += `<option value="${key}">${key.toUpperCase()}</option>`;
}

// CONVERT
function convertText(text, style) {
  if (style === "normal") return text;
  const map = maps[style];
  return text
    .split("")
    .map((char) => map[char] || char)
    .join("");
}

// LIVE UPDATE
function updateOutput() {
  outputText.value = convertText(inputText.value, fontStyle.value);
}

inputText.addEventListener("input", updateOutput);
fontStyle.addEventListener("change", updateOutput);

